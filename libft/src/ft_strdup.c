#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	len;
	char	*dup;

	len = ft_strlen(str);
	dup = ft_calloc(sizeof(char), len + 1);
	if (!dup)
		return (NULL);
	len = -1;
	while (str[++len])
		dup[len] = str[len];
	return (dup);
}
